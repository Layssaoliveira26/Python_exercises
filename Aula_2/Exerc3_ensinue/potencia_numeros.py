#Receber dois números e calcular o quadrado de cada um deles
#Receber os numeros
num1 = float(input("Digite o primeiro número: "))
num2 = float(input("Digite o segundo número: "))
#Calcular as deles e imprimir
print(f"A potência de {num1} é: ", num1 ** 2)
print(f"A potência de {num2} é: ", num2 ** 2) 